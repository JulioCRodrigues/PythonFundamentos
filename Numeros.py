# Numeros e operações matemáticas

num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo numero: "))

# Soma
soma = int(num1 + num2)
print("O resultado da soma é: " + str(soma))
type(soma)

# Subtração
sub = int(num1 - num2)
print("O resultado da subtração é: " + str(sub))

# Multiplicação
mult = int(num1 * num2)
print("O resultado da multiplicação é: " + str(mult))

# Divisão
div = int(num1 / num2)
print("O resultado da divisão é: " + str(div))

# Conversão para decimal
print(float(9))

# Conversão para inteiro
print(int(7.5))

# Potencia
print(pow(5, 2))

# Arredondar
print(round(3.5))