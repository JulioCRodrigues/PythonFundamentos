# Variaveis e operadores
# Exibir na tela
var_teste = 50
print(var_teste)

var_texto = "Olá, mundo!"
print(var_texto)

#Exibe tipo da variavel = inteiro
print(type(var_teste))

#Exibe tipo da variavel = string
print(type(var_texto))

#Declaração multipla
pessoa1, pessoa2, pessoa3 = "Maria", "José", "Paulo"
print(pessoa1, pessoa2, pessoa3)

#Usando variaveis para operações
altura = 4
largura = 2
area = altura * largura
print(area)

#Concatenar variaveis
nome = "Julio"
sobrenome = "Rodrigues"

fullname = nome  + " " + sobrenome
print(fullname)